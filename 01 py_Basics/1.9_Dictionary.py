# Dictionary

# it is a unorder collection of key-value pairs.

a={1:'vishnu',2:'Harsh',3:'Mangesh',4:'sanket'}
b= type(a)
print(b);           # <class 'dict'>
print(a);                                       # {1: 'vishnu', 2: 'Harsh', 3: 'Mangesh', 4: 'sanket'}

print(a[2]);        # Harsh

print(a[4]);        # sanket



